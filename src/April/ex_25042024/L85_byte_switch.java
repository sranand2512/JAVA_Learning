package April.ex_25042024;

public class L85_byte_switch {
    public static void main(String[] args) {

        byte b = 20;

        switch (b){
            case 10:
                System.out.println("Ten");
                break;
            case 50:
                System.out.println("fifty");
                break;
            case 100:
                System.out.println("hundred");
                break;
            default:
                System.out.println("default");
        }

    }
}
