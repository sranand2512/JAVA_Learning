package ex_18042024;

public class L028_arithmetic_operators {
    public static void main(String[] args) {
        int a = 40;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
