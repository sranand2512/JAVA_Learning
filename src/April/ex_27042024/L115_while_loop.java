package April.ex_27042024;

public class L115_while_loop {
    public static void main(String[] args) {
        //while loop
        //A- Initialization
        //B- condition
        // c - Increment

        int i = 1; // initialization
        while(i<=10){ // condition
            System.out.println(i);
            // executable
            i++ ;// Increment / decrement
        }
    }

}
