package April.ex_18042024;

public class L033_relational_equals {
    public static void main(String[] args) {

        System.out.println(10==10); // == means exact match
        System.out.println(10>=10); // == means > or =
        System.out.println(10<=10); // == means < or =
        System.out.println(10<10); // == means less only
        System.out.println(10>10); // == means > only


        System.out.println("_________");
        System.out.println('S');
        System.out.println('S'==65);
        System.out.println('S'==65.2);
       // System.out.println('S'65);

       // == means check if it is equal or not
        // = ia assign the value

       // System.out.println(true>=true);
        // booleans can not be compared


    }
}
