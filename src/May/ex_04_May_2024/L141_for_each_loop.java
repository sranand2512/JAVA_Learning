package May.ex_04_May_2024;

public class L141_for_each_loop {
    public static void main(String[] args) {
        int [] arr = {66,52,6,6,5};
        for (int abc : arr){
            System.out.println(abc);
        }
        System.out.println("_");

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);

        }
    }
}
