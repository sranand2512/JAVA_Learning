package April.ex_27042024;

public class L100_for_loop {
    public static void main(String[] args) {
        // conditions
        // if, else
        // if, else if, else
        //switch, break


        // For loop, While loop, Do while loop
        // Basically loops is used for performing repetitive task


        // for (A:B:C) works on this principle
        // A - > initialization -> from where you want to start
        // B -> Condition - where you want to stop
        // C - > how you want to increment / decrement

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
            System.out.println("Code you want to execute multiple time");

        }
    }
}
